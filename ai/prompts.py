    """Промт для сводного мульти-ТФ анализа по РЕГЛАМЕНТУ (18 разделов).

    Args:
        consolidated: результат pipeline.multi_tf.consolidate()
        per_tf_reports: dict {tf_label: report_text} — (для совместимости, не используется,
            так как AI получает уже сформированный pre_report)
        horizon: горизонт прогноза из ANALYSIS_TYPES

    Returns:
        (system_prompt, user_message)
    """
    import json
    import numpy as np

    def _np_default(obj):
        if isinstance(obj, (np.bool_,)):
            return bool(obj)
        if isinstance(obj, (np.integer,)):
            return int(obj)
        if isinstance(obj, (np.floating,)):
            return float(obj)
        return str(obj)

    from pipeline.multi_tf import format_multi_tf_report
    pre_report = format_multi_tf_report(consolidated)

    # Собираем все section_id, которые встречаются в данных
    per_tf_entries = consolidated.get("per_tf", [])
    all_sids = set()
    for e in per_tf_entries:
        for s in e.get("sections_data", []):
            all_sids.add(s.get("section_id"))
    all_sids.discard(None)
    section_ids = sorted(all_sids) if all_sids else list(range(1, 19))

    analysis_type_name = consolidated.get("analysis_type_name", "—")

    # Базовый регламент + контекст анализа (горизонт/тип)
