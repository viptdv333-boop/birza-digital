def load_system_prompt():
    """Загрузить системный промпт из регламента (v8 приоритетно, v6 fallback)."""
    import os
    base = os.path.join(os.path.dirname(__file__), "..")
    # v8 — ПРИОРИТЕТ
    for name in ("regulation_v8.json", "regulation_v7.json", "regulation_v6.json"):
        reg_path = os.path.join(base, name)
        if os.path.exists(reg_path):
            with open(reg_path, encoding="utf-8") as f:
                reg_text = f.read()
            return _SYSTEM_PROMPT_PREFIX + reg_text
    return _SYSTEM_PROMPT_PREFIX


