def build_ai_conclusion_prompt(analysis_json: dict, pre_report: str = None,
                                sections_map: dict = None, meta: dict = None) -> tuple[str, str]:
    """Промпт для генерации вывода ИИ по данным препроцессора."""
    meta = meta or {}
    ticker = meta.get("ticker", "—")
    exchange = meta.get("exchange", "—")
    tf = meta.get("tf", meta.get("timeframe", "—"))
    price = meta.get("price", meta.get("current_price", 0))
    date = meta.get("date", "—")
    analysis_type = meta.get("analysis_type_name") or meta.get("analysis_type") or _infer_analysis_type(tf)
    ticker_name = meta.get("ticker_name") or _ticker_display_name(ticker)

    full_report = pre_report or "Отчёт недоступен"

    # Краткая сводка мат.модели
    math_summary = "Нет данных"
    if sections_map and 20 in sections_map:
        s20 = sections_map[20].get("data", {})
        parts = []
        mp = s20.get("model_probs", {})
        if mp:
            avg_up = sum(p.get("p_up", 0) for p in mp.values()) / max(len(mp), 1)
            avg_down = sum(p.get("p_down", 0) for p in mp.values()) / max(len(mp), 1)
            parts.append(f"Консенсус: P(up)={avg_up:.0%}, P(down)={avg_down:.0%}")
        manip = s20.get("manipulation", {})
        if manip.get("score"):
            parts.append(f"Манипуляция: score={manip['score']:.2f}, направление={manip.get('direction', '?')}")
        pde = s20.get("pde", {})
        if pde.get("P50"):
            parts.append(f"PDE: P10={pde.get('P10', 0):.4g}, P50={pde.get('P50', 0):.4g}, P90={pde.get('P90', 0):.4g}")
        math_summary = "\n".join(parts) if parts else "Нет данных"

    user_msg = AI_CONCLUSION_USER.format(
        ticker=ticker, exchange=exchange, tf=tf,
        price=price, date=date,
        full_report=full_report,
        math_summary=math_summary,
        analysis_type=analysis_type,
        ticker_name=ticker_name,
    )
    return AI_CONCLUSION_SYSTEM, user_msg
