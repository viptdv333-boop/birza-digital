def build_user_message(preprocessor_json: dict) -> str:
    """Сформировать user message из JSON препроцессора."""
    import json
    import numpy as np

    def _default(obj):
        if isinstance(obj, (np.bool_,)):
            return bool(obj)
        if isinstance(obj, (np.integer,)):
            return int(obj)
        if isinstance(obj, (np.floating,)):
            return float(obj)
        return str(obj)

    return (
        "Проанализируй данные и выдай полный отчёт по всем разделам.\n\n"
        "```json\n"
        + json.dumps(preprocessor_json, ensure_ascii=False, indent=2, default=_default)
        + "\n```"
    )


def build_system_prompt_with_sections(section_ids: list[int],
                                       horizon: str = None,
                                       analysis_type_name: str = None) -> str:
    """Системный промт + инструкции для конкретных разделов.

    Args:
        section_ids: список ID разделов
        horizon: горизонт прогноза из ANALYSIS_TYPES (напр. "1-2 сессии")
        analysis_type_name: название типа анализа (напр. "Краткосрок")
    """
    parts = [load_system_prompt()]
    if horizon or analysis_type_name:
        ctx = "\n## КОНТЕКСТ АНАЛИЗА\n"
        if analysis_type_name:
            ctx += f"Тип анализа: **{analysis_type_name}**\n"
        if horizon:
            ctx += f"Горизонт прогноза: **{horizon}**\n"
        ctx += "Ориентируй выводы и сроки под этот горизонт.\n"
        parts.append(ctx)
    for sid in sorted(section_ids):
        if sid in SECTION_PROMPTS:
            parts.append(SECTION_PROMPTS[sid])
    return "\n\n".join(parts)


