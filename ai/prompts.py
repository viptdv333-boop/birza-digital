def _infer_analysis_type(tf) -> str:
    """Определить тип анализа по ТФ (по ЯДРУ v8 analysis_types).
    Если tf — список/множество, выбираем тип по полному набору."""
    if not tf:
        return "Основной"
    if isinstance(tf, (list, tuple, set)):
        tfs = {str(t).strip().lower() for t in tf}
        # Основной — полный набор
        base = {"15m", "15мин", "1h", "1час", "4h", "4часа", "1d", "1день", "1w", "1неделя"}
        if len(tfs & base) >= 4:
            return "Основной"
        if tfs & {"1w", "1неделя", "1m", "1мес"}:
            return "Долгосрок"
        if tfs & {"1d", "1день"}:
            if tfs & {"4h", "4часа", "1w", "1неделя"}:
                return "Среднесрок"
            return "Среднесрок-экспресс"
        if tfs & {"4h", "4часа"}:
            if tfs & {"1h", "1час"} or tfs & {"15m", "15мин"}:
                return "Краткосрок"
            return "Неделя-экспресс"
        if tfs & {"1h", "1час"}:
            return "Краткосрок-экспресс"
        return "Интрадей-экспресс"
    s = str(tf).strip().lower()
    return _ANALYSIS_TYPE_BY_TF.get(s, _ANALYSIS_TYPE_BY_TF.get(s.replace(" ", ""), "Основной"))


def _ticker_display_name(ticker: str) -> str:
    """Отобразимое имя актива для вступительной фразы вывода."""
    if not ticker:
        return "актив"
    t = str(ticker).upper().strip().lstrip("#").split("_")[-1]
    # убираем биржу из префикса "NYMEX:NG1!"
    if ":" in t:
        t = t.split(":")[-1]
    return _TICKER_DISPLAY_NAME.get(t, t)

